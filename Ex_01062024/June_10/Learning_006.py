# Arithmatic Operatore
# +, -, *, /, %, //, **
a = 90
b = 20
print(a + b)
print(a - b)
print(a * b)
print(a / b)
print(a % b)
print(a // b)
print(a ** b)

# Logical Operators
# and, or, not
x = 10
y = 20
print(x > y)
print(x < y)
print(x == y)
print(x != y) # ! .>> NOT
print(x >= y)
print(x <= y)

print(x and y) # Refer to Truth Table
print(x or y)
print(not x)
print(not y)


