# lambda expression
# to do the task in one line
# we can use lambda expression for short and concise code

f_double_my_salary = lambda x: x*2
print(f_double_my_salary(40000))

f_sum_of_two = lambda x,y: x+y
print(f_sum_of_two(10,20))

f_result_of_num = lambda x,y: (x+y, x-y, x*y, x/y)
print(f_result_of_num(10, 20))

